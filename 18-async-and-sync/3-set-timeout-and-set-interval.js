/** ### SET TIMEOUT AND SET INTERVAL ### */

/** setTimeout */
setTimeout(() => {
    console.log('We can see this in 3 seconds');
}, 3000);

setTimeout(() => {
    console.log('We can see this in 1 seconds');
}, 1000);

/** setInterval */
setInterval(() => {
    console.log('You can see this text every second')
}, 1000);